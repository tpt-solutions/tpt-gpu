#[cfg(test)]
mod tests {
    use super::*;
    use crate::compile_str;

    fn rust_emit(src: &str) -> String {
        let prog = compile_str(src).expect("compile_str");
        RustEmitter::new().emit_program(&prog)
    }

    #[test]
    fn test_simple_fn() {
        let out = rust_emit("fn add(a: f32, b: f32) -> f32 { return a + b }");
        assert!(out.contains("pub fn add(a: f32, b: f32) -> f32"), "{out}");
        assert!(out.contains("return a + b;"), "{out}");
    }

    #[test]
    fn test_tpt_call_rewritten() {
        let out = rust_emit("fn f(x: Tensor[f32, m, n]) { let y = tpt.relu(x) }");
        assert!(out.contains("tptr::relu(x)"), "{out}");
        assert!(!out.contains("tpt.relu"), "{out}");
    }

    #[test]
    fn test_tpt_call_with_named_args() {
        let out = rust_emit("fn f() { let z = tpt.zeros([4, 4], dtype=f32) }");
        assert!(out.contains("tptr::zeros("), "{out}");
    }

    #[test]
    fn test_gpu_fn_skipped() {
        let out = rust_emit("@requires_gpu(true) fn kernel() { }");
        assert!(!out.contains("pub fn kernel"), "{out}");
        assert!(out.contains("GPU kernel `kernel`"), "{out}");
    }

    #[test]
    fn test_import_emitted() {
        let out = rust_emit("import tpt::nn\nfn f() { }");
        assert!(out.contains("use tpt::nn;"), "{out}");
    }

    #[test]
    fn test_tensor_type_mapping() {
        let out = rust_emit("fn f(x: Tensor[f32, m, n]) -> Tensor[f32, m, n] { return x }");
        assert!(out.contains("tptr::Tensor<f32>"), "{out}");
    }

    #[test]
    fn test_doc_comment_emitted() {
        let out = rust_emit(r#"@doc("Compute ReLU") fn relu(x: f32) -> f32 { return x }"#);
        assert!(out.contains("/// Compute ReLU"), "{out}");
    }

    #[test]
    fn test_for_loop() {
        let out = rust_emit("fn f() { for i in 0..10 { let x = i } }");
        assert!(out.contains("for i in 0..10"), "{out}");
    }

    #[test]
    fn test_if_else() {
        let out = rust_emit("fn f(b: bool) -> i32 { if b { return 1 } else { return 0 } }");
        assert!(out.contains("if b"), "{out}");
    }

    #[test]
    fn test_let_with_type_annotation() {
        let out = rust_emit("fn f() { let x: f32 = 1.0 }");
        assert!(out.contains("let x: f32 = "), "{out}");
    }

    #[test]
    fn test_full_matmul_roundtrip() {
        let src = r#"
@doc("Matrix multiply A × B")
@requires_gpu(true)
@constraint("a.shape[1] == b.shape[0]", error="Inner dimensions must match")
fn matmul(a: Tensor[f32, m, k], b: Tensor[f32, k, n]) -> Tensor[f32, m, n] {
    let result = tpt.zeros([m, n], dtype=f32)
    tpt.gemm(a, b, result)
    return result
}

fn scale(x: f32, factor: f32) -> f32 {
    return x * factor
}
"#;
        let out = rust_emit(src);
        // GPU kernel skipped
        assert!(out.contains("GPU kernel `matmul`"), "{out}");
        // Host fn emitted
        assert!(out.contains("pub fn scale(x: f32, factor: f32) -> f32"), "{out}");
    }
}
